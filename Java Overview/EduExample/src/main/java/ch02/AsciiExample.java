package ch02;

public class AsciiExample {
    public static void main(String[] args) {
        System.out.println("abc\t123");     // \t에 탭 문자 실행
        System.out.println("\n");           // 개행(new line)문자 출력
        System.out.println("\"Hello\"");    // 큰따옴표 출력
        System.out.println("c:\\");         // \(slash) 표시
    }
}
