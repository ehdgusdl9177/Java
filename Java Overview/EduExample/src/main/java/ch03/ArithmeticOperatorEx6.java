package ch03;

public class ArithmeticOperatorEx6 {
    public static void main(String[] args) {
        int i = 10;
        int j = 20;

        i += 4;     // i = i + 4; (i = 10 + 4)
        j -= 4;     // j = j - 4; (j = 20 - 4)

        System.out.println(i);
        System.out.println(j);
    }
}
