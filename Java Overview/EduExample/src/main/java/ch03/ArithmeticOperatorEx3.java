package ch03;

public class ArithmeticOperatorEx3 {
    public static void main(String[] args) {
        int i = 10;
        int j = 20;

        System.out.println(i == j);
        System.out.println(i != j);
        System.out.println(i < j);
        System.out.println(i <= j);
        System.out.println(i > j);
        System.out.println(i >= j);
    }

}
