package ch03;

public class ArithmeticOperatorEx1 {
    public static void main(String[] args) {
        int num1 = 31;
        int num2 = 6;

        System.out.println("+ 연산 : " + (num1 + num2));
        System.out.println("- 연산 : " + (num1 - num2));
        System.out.println("* 연산 : " + (num1 * num2));
        System.out.println("/ 연산 : " + (num1 / num2));
        System.out.println("% 연산 : " + (num1 % num2));

    }
}
